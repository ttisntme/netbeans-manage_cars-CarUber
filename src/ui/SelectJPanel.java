    private void displayTable(CarFleet newFleet) {
        DefaultTableModel model = (DefaultTableModel) tblShowResult.getModel();
        model.setRowCount(0);
        
        for(Car che: newFleet.getCarList()){
            Object[] row = new Object[9];
            row[0] = che.getSerialNumber();  
            int t = che.getAvailTime();
            int j = t / 100;
            int k = t % 100;
            if(k==0){
                row[1] = j + ":" + k + "0";
            }else row[1] = j + ":" + k;
            if(che.getAvailFlag()==1){
                row[2] = "Yes";
            }else row[2] = "No";
            row[3] = che.getMadeCompany();
            if(che.getYear()==0){
                row[4] = null;
            }else row[4] = che.getYear();
            if(che.getSeat()==0){
                row[5] = null;
            }else row[5] = che.getSeat();
            row[6] = che.getModelNumber();
            row[7] = che.getCity();
            if(che.getMaintenanceState()==1){
                row[8] = "Valid";
            }else row[8] = "Expired";
            
            model.addRow(row);
        }
        
    }
