    private void btnFindFirstAvailCarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindFirstAvailCarActionPerformed
        // TODO add your handling code here:
        
        Car targetCar = new Car();
        int tempTime = 2401;
        for(Car temp : carFleet.getCarList()){
            if(temp.getAvailTime()<tempTime){
                targetCar = temp;
                tempTime = temp.getAvailTime();
            }
        }
        JOptionPane.showMessageDialog(this, "Successfully find the first Car available.");
        String temp1 = targetCar.getSerialNumber();
        int temp2 = targetCar.getAvailTime();
        int j = temp2 / 100;
        int k = temp2 % 100;
        String result;
        if(k == 0){
            result = "No." + temp1 + " : avail at " + j + ":" + k + "0";
        }else{
            result = "No." + temp1 + " : avail at " + j + ":" + k;
        }
        txtFindFirstAvailCar.setText(result);
        
    }//GEN-LAST:event_btnFindFirstAvailCarActionPerformed

